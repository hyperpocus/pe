--creazione tabelle
	
create table People (
	PersonID number --generated by default as identity
	,FirstName varchar2 (50)
	,LastName varchar2(50) not null
	,BirthDate date not null
	,DepartmentId number
	,primary key(PersonID)
);

create table Departments (
	DepartmentId number --generated by default as identity
	,Name varchar2(50) not null
	,Description varchar2(100)
	,AreaID number not null
	,primary key (DepartmentId)
);

create table Areas(
	AreaID number --generated by default as identity
	,Name varchar2(50) not null
	,Location varchar2(50) not null
	,primary key (AreaID)
);
	
create table People_Roles(
	PersonID number
	,RoleID number
);

create table Roles(
	RoleID number not null --generated by default as identity
	,Name varchar2(50) not null
	,Description varchar2(100)
	,primary key (RoleID)
);

--creazione sequenze
create sequence seq_departments start with 10 increment by 10;
create sequence seq_peoples start with 1 increment by 1;
create sequence seq_areas start with 1000 increment by 100;
create sequence seq_roles start with 10 increment by 10;

--creazione foreign keys
alter table People add constraint department_id_fk foreign key (DepartmentId) references Departments(DepartmentId);
alter table Departments add constraint area_id_fk foreign key (AreaID) references Areas(AreaID);
alter table People_Roles add constraint person_id_fk foreign key (PersonID) references People(PersonID);
alter table People_Roles add constraint role_id_fk foreign key (RoleID) references Roles(RoleID);

